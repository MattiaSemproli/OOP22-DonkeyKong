package it.unibo.donkeykong.utilities;

/**
 * Enum representing every existing type.
 */
public enum Type {
    /**
     * Player.
     */
    PLAYER,
    /**
     * Level's boss.
     */
    MONKEY,
    /**
     * Princess.
     */
    PRINCESS,
    /**
     * Barrel.
     */
    BARREL,
    /**
     * Block, like roof or floor.
     */
    BLOCK,
    /**
     * Ladder.
     */
    LADDER,
    /**
     * Block with upper ladder.
     */
    BLOCK_LADDER_UP,
    /**
     * Block with lower ladder.
     */
    BLOCK_LADDER_DOWN,
    /**
     * Block with both ladder.
     */
    BLOCK_LADDER_UPDOWN,
    /**
     * Shield.
     */
    SHIELD,
    /**
     * Extra life.
     */
    HEART,
    /**
     * Immortality.
     */
    STAR,
    /**
     * Freeze Donkey Kong.
     */
    SNOWFLAKE,
}
