package it.unibo.donkeykong.utilities;

/**
 * Enum for every existing type.
 */
public enum Type {
    /**
     * Player.
     */
    PLAYER,
    /**
     * Boss of the level.
     */
    MONKEY,
    /**
     * Princess.
     */
    PRINCESS,
    /**
     * Barrel.
     */
    BARREL,
    /**
     * Block, like roof or floor.
     */
    BLOCK,
    /**
     * Block empty, nothing exist there.
     */
    BLACK_BLOCK,
    /**
     * Ladder.
     */
    LADDER,
    /**
     * Block with ladder.
     */
    BLOCK_LADDER,
    /**
     * Powerup.
     */
    POWER_UP,
}