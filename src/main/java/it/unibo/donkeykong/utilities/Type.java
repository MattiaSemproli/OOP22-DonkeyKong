package it.unibo.donkeykong.utilities;

/**
 * Enum for every existing type.
 */
public enum Type {
    /**
     * Player.
     */
    PLAYER,
    /**
     * Boss of the level.
     */
    MONKEY,
    /**
     * Princess.
     */
    PRINCESS,
    /**
     * Barrel.
     */
    BARREL,
    /**
     * Block, like roof or floor.
     */
    BLOCK,
    /**
     * Ladder.
     */
    LADDER,
    /**
     * Block with upper ladder.
     */
    BLOCK_LADDER_UP,
    /**
     * Block with lower ladder.
     */
    BLOCK_LADDER_DOWN,
    /**
     * Block with both ladder.
     */
    BLOCK_LADDER_UPDOWN,
    /**
     * Powerup.
     */
    POWER_UP,
}
